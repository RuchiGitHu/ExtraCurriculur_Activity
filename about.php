<h2 class="font-weight-bold">OUR MISSION </h2>
<p>The Seven Arrows Way is based on the profound pedagogy and philosophy of Experiential Learning. We ground our educational approach in leading-edge brain research. The Theory of Multiple Intelligences, developed at Harvard University by cognitive scientist Dr. Howard Gardner, holds that different people have diverse ways of learning, remembering, performing, processing, and understanding information. Gardner posits that there are seven distinct intelligences: Visual-Spatial, Bodily-Kinesthetic, Musical, Interpersonal, Intrapersonal, Linguistic, and Logical-Mathematical. Our curriculum is designed to explore these intelligences, and to identify each child’s individual learning style, in order to optimize his or her educational results.

We also draw upon Dr. Richard Davidson’s research in affective neuroscience, which focuses on how a child’s emotional state affects the way he or she absorbs information. 
