 <h1>The Definition of Extracurricular Activities</h1>
<p>Before we get into the nitty gritty, let’s take a deeper look at the definition of extracurricular activities.

The word extracurricular is a combination of the prefix extra, which translates to “on the outside” and curriculum, which translates to “a running course/career.”
However, simply playing soccer with your friends on the weekends for fun isn’t actually an extracurricular activity, even though it has nothing to do with school.

Extracurricular activities require a regular time commitment and initiative such as being on a sports team, forming a student newspaper, playing the violin in the local orchestra or taking an online course on robotics.
<h1>The Benefits of Extracurricular Activities</h1>

<h1>1. Improved Academic Performance</h1>
<p>Some students worry that participating in extracurriculars may take away too much time from their schoolwork, thus hurting their grades; however, extracurricular activities can actually improve your grades and your outlook on school in general!</p>
<p>
Participating in activities you are passionate about can increase your brain function, help you concentrate and manage your time better, all of which contribute to higher grades. High endurance sports, for example, will train you to focus and build stamina in the face of intense difficulty. This gives you an advantage when it comes to studying and taking exams.</p>

<h1>2. Explore Interests and Create Broader Perspectives</h1>
<p>When you participate in multiple different activities, you’ll get the opportunity to explore a range of interests and unlock passions you never knew you had!

Plus, diversifying your interests subsequently broadens your world view.

Think about it this way: if you join a philosophy club you’ll begin to look at the world through the eyes of a budding philosopher.</p>

<h1>3. Higher Self-esteem</h1>
<p>The more you achieve success through activities you’re passionate about, the more your self confidence will improve.

For example, let’s say you’re really good at maths and your teacher encourages you to get involved in competitions. You join the school team and start training for the national Maths Olympiad. During the process you realise how fun maths can be and how talented you actually are, which gives your confidence a massive boost.

Working hard and mastering new skills in a fun, relaxed – and sometimes competitive – setting allows you to be successful without the pressure of getting a good grade.

Plus, once your confidence improves, you’ll be more open to taking risks in all aspects of your life, not just in Maths Olympiads.</p>
<h1>4. Essential Life Skills</h1>
<p>On top of all of the benefits of extracurricular activities we already talked about, one of the greatest advantages extracurricular activities give you are “real world” skills.
<p>The more you push yourself in your extracurricular endeavours, the more you’ll develop these skills. If you’re passionate about coding, you might join the school coding club, where you’ll develop teamwork, problem solving and analytical thinking skills.

But you could take that passion even further and create your own coding club, where you’ll develop goal setting, time management, prioritisation, leadership and public speaking skills.

Phew! That’s a lot of responsibility. Each and every step will teach you a new skill you can use for the rest of your life.</p>
